// Helper function to get all stations flat
export const getAllStations = (): RadioStation[] => {
  return radioGenres.flatMap(genre => genre.stations);
};

// Helper function to get station by ID
export const getStationById = (id: string): RadioStation | undefined => {
  return getAllStations().find(station => station.id === id);
};

// Helper function to get genre by ID
export const getGenreById = (id: string): RadioGenre | undefined => {
  return radioGenres.find(genre => genre.id === id);
};

// Helper function to search stations
export const searchStations = (query: string): RadioStation[] => {
  const lowerQuery = query.toLowerCase();
  return getAllStations().filter(station =>
    station.name.toLowerCase().includes(lowerQuery) ||
    station.description.toLowerCase().includes(lowerQuery) ||
    station.country.toLowerCase().includes(lowerQuery) ||
    station.tags.some(tag => tag.toLowerCase().includes(lowerQuery))
  );
};
