// Helper function to get all stations flat
export const getAllStations = (): RadioStation[] => {
  return radioGenres.flatMap(genre => genre.stations);
};

// Helper function to get station by ID
export const getStationById = (id: string): RadioStation | undefined => {
  return getAllStations().find(station => station.id === id);
};

// Helper function to get genre by ID
export const getGenreById = (id: string): RadioGenre | undefined => {
  return radioGenres.find(genre => genre.id === id);
};

// Helper function to search stations
export const searchStations = (query: string): RadioStation[] => {
  const lowerQuery = query.toLowerCase();
  return getAllStations().filter(station =>
    station.name.toLowerCase().includes(lowerQuery) ||
    station.description.toLowerCase().includes(lowerQuery) ||
    station.country.toLowerCase().includes(lowerQuery) ||
    station.tags.some(tag => tag.toLowerCase().includes(lowerQuery))
  );
};

// Helper function to get a random station from all genres
export const getRandomStation = (): RadioStation => {
  const allStations = getAllStations();
  return allStations[Math.floor(Math.random() * allStations.length)];
};

// Helper function to get a random station from a specific genre
export const getRandomStationFromGenre = (genreId: string): RadioStation | undefined => {
  const genre = getGenreById(genreId);
  if (!genre || genre.stations.length === 0) return undefined;
  return genre.stations[Math.floor(Math.random() * genre.stations.length)];
};

// Helper function to shuffle an array
export const shuffleStations = (stations: RadioStation[]): RadioStation[] => {
  const shuffled = [...stations];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
};
